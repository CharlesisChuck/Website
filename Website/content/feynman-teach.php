	<h2>Feynman's Teachings</h2>

	<p>Since the majority of his lessons are within the three volumes of physics that is what I will talk about here.</p>

	

	<a href="http://www.feynmanlectures.caltech.edu/I_toc.html" class="#"><h4 style="text-transform:uppercase">Book 1: mainly mechanics, radiation, and heat</h4></a>
	<p>If you've never touched physics or but have been curious this is were to start!</p>
	<p>Amazingly you could start this with literally zero background in physics and by the end of this book you'll know more than most college students.</p>
	<p>The first quarter of the book is quite simple but when it picks up it gets interesting!</p>
	<p>Anyone learning optics or quantum mechanics should be using this book as a first level of learning.</p>

	<a href="http://www.feynmanlectures.caltech.edu/II_toc.html" class="#"><h4 style="text-transform:uppercase">Book 2 mainly electromagnetism and matter</h4></a>
	<p>This is where you may start realizing Feynman is ALOT smarter than you are. His later lectures on solutions of maxwell's equations are easily the hardest things I have ever studied.</p>
	<p>However, surviving the maxwell's equations gives a very amazing reward of completely unique way of viewing electronics, waveguides and antenna.</p>
	<p>Other great parts of this book are materials and magnetism.</p>

	<a href="http://www.feynmanlectures.caltech.edu/III_toc.html" class="#"><h4 style="text-transform:uppercase">Book 3 quantum mechanics</h4></a>
	<p>If you finish this you beat me! I've only read a few chapters of this book as I don't believe I am ready for it yet. Someday I will finish this whole books and then I'll reread it all!</p>
	<p>However, you don't need too high level knowledge for his semiconductor lessons as long as you know how crystals work with light propogation.</p>

